#!/bin/bash

# ============================================================
# TLS 1.2 / TLS 1.3 フルハンドシェイク性能比較スクリプト
# ============================================================
#
# このスクリプトは、OpenSSL の `s_client` を繰り返し実行することで、
# 一定時間内に何回 TLS 接続を張れるかを測定する簡易ベンチマークです。
#
# 具体的には次の 2 つを比較します。
#
#   1. TLS 1.2 のフルハンドシェイク
#   2. TLS 1.3 のフルハンドシェイク
#
# ここでいう「フルハンドシェイク」とは、
# セッション再開や 0-RTT を使わず、毎回最初から鍵交換・証明書検証を含む
# 完全な TLS 接続確立を行うことを意味します。
#
# このスクリプトのアルゴリズムは次の通りです。
#
#   1. 開始時刻を記録する
#   2. `openssl s_client` で TLS 接続を 1 回実行する
#   3. 成功/失敗に関係なく試行回数を 1 増やす
#   4. 現在時刻との差から経過秒数を計算する
#   5. 30 秒を超えるまで繰り返す
#   6. 総接続回数を表示する
#   7. TLS 1.2 と TLS 1.3 で同じ処理を行い、比較する
#
# 図で表すと次のようになります。
#
#   開始時刻取得
#       ↓
#   while ループ開始
#       ↓
#   openssl s_client 実行
#       ↓
#   count++
#       ↓
#   経過秒数計算
#       ↓
#   30秒超過?
#     ├─ No → ループ継続
#     └─ Yes → 結果表示
#
# ベンチマークとしての意味:
# - 接続 1 回あたりの厳密なレイテンシ測定ではない
# - 一定時間内の総試行回数を見ることで「ざっくりした接続性能」を比較する
# - TLS 1.2 と TLS 1.3 のフルハンドシェイクの差を体感する用途に向いている
#
# 注意点:
# - この方法はサーバ側負荷や Docker ネットワーク状況にも影響される
# - `date +%s` は秒単位なので、細かい計測精度は高くない
# - `> /dev/null 2>&1` で出力を捨てているため、失敗の詳細は見えない
# - 1 秒未満の差や平均接続時間を厳密に測るなら `time` や `hyperfine`、
#   あるいはミリ秒/ナノ秒単位の計測手法を使う方がよい
#
# また、元のスクリプトではバッククォート記法 `` `...` `` が使われていましたが、
# 可読性と保守性の観点から、この修正版では `$(...)` 形式に補完しています。
# 動作上の意味は同じですが、現在はこちらの方が一般的です。


# ------------------------------------------------------------
# TLS 1.2 計測用の初期化
# ------------------------------------------------------------

count=0
# count:
# 何回 `openssl s_client` を実行できたかを数えるカウンタ。
# 1 回接続するごとに 1 増加する。

elapsed=0
# elapsed:
# 開始から何秒経過したかを保持する変数。
# `date +%s` の差分で求める。

start_time=$(date +%s)
# start_time:
# 計測開始時刻を UNIX 時間（秒）で記録する。
#
# `date +%s`
#   現在時刻を「1970-01-01 00:00:00 UTC からの経過秒数」で返す。
#
# これを基準に、各ループで現在時刻との差を取ることで経過秒数を計算する。


# ------------------------------------------------------------
# TLS 1.2 フルハンドシェイクの繰り返し計測
# ------------------------------------------------------------

while true
do
  # openssl s_client:
  #   OpenSSL のクライアントとして TLS 接続を行うコマンド。
  #
  # -connect booktls-server:10443
  #   接続先ホストとポートを指定する。
  #   ここでは Docker ネットワーク内の `booktls-server` に対して
  #   10443 番ポートで接続している。
  #
  # -tls1_2
  #   TLS 1.2 を強制する。
  #   これにより、同じサーバに対して TLS 1.2 のみで接続性能を測れる。
  #
  # -CAfile ca.pem
  #   サーバ証明書を検証するための CA 証明書ファイルを指定する。
  #   自作 CA を使った学習環境では必須になる。
  #
  # <<< "Q"
  #   これは bash のヒアストリングで、標準入力として "Q" を 1 回渡している。
  #   `s_client` は対話的に標準入力を待つことがあるため、
  #   ここでは何らかの入力を即座に送って接続を閉じやすくしている。
  #
  # > /dev/null 2>&1
  #   標準出力・標準エラー出力の両方を捨てる。
  #   ベンチマーク中に大量の出力が画面へ流れるのを防ぐためである。
  #
  # この 1 行の本質:
  #   「TLS 1.2 の完全な接続を 1 回行い、結果は表示せずに破棄する」
  openssl s_client -connect booktls-server:10443 -tls1_2 -CAfile ca.pem <<< "Q" > /dev/null 2>&1

  ((count++))
  # 接続試行が 1 回終わったのでカウンタを増やす。
  #
  # 注意:
  # 現在の書き方だと、接続失敗していても count は増える。
  # つまりこれは「成功した接続回数」ではなく
  # 「試行した回数」に近い指標である。
  #
  # もし成功回数だけ数えたいなら、
  # `openssl s_client ...` の終了ステータスを見て条件分岐する必要がある。

  now=$(date +%s)
  # 現在時刻を UNIX 時間で取得する。

  elapsed=$((now - start_time))
  # 開始時刻との差分を取り、経過秒数を求める。

  if [ "$elapsed" -gt 30 ]; then
    # 経過秒数が 30 秒を超えたら計測終了。
    #
    # `-gt 30`
    #   greater than 30 を意味する整数比較。
    #
    # 注意:
    # 厳密には 30 秒ちょうどでは止まらず、
    # 31 秒になった時点などで止まる可能性がある。
    # 秒単位計測なので、おおまかなベンチマークと考えるべきである。
    break
  fi
done

echo "TLS1.2 full-handshake: ${count} connections in ${elapsed} real seconds"
# TLS 1.2 の結果表示。
#
# 例:
#   TLS1.2 full-handshake: 250 connections in 31 real seconds
#
# これは
#   「約 31 秒間で 250 回接続試行できた」
# という意味である。


sleep 3
# TLS 1.2 計測直後にすぐ TLS 1.3 計測へ移ると、
# サーバ側や OS 側の状態（TIME_WAIT, キャッシュ, 一時的負荷など）が
# 影響することがあるため、3 秒待って少し落ち着かせている。
#
# 完全に公平とは言えないが、連続実行時の雑な干渉を減らす意図がある。


# ------------------------------------------------------------
# TLS 1.3 計測用の初期化
# ------------------------------------------------------------

count=0
# TLS 1.3 用にカウンタをリセットする。

elapsed=0
# 経過時間もリセットする。

start_time=$(date +%s)
# TLS 1.3 計測の開始時刻を新たに記録する。


# ------------------------------------------------------------
# TLS 1.3 フルハンドシェイクの繰り返し計測
# ------------------------------------------------------------

while true
do
  # TLS 1.2 計測とほぼ同じだが、ここでは
  #
  #   -tls1_3
  #
  # を指定することで TLS 1.3 のみを使って接続する。
  #
  # TLS 1.3 は TLS 1.2 と比べてハンドシェイクが簡略化されているため、
  # フルハンドシェイクでも一般に効率が良いことが期待される。
  #
  # このループにより、
  #   「同じサーバ・同じ CA 検証条件で、TLS 1.3 の接続試行を何回こなせるか」
  # を測定する。
  openssl s_client -connect booktls-server:10443 -tls1_3 -CAfile ca.pem <<< "Q" > /dev/null 2>&1

  ((count++))
  # 試行回数を 1 増やす。
  # ここも TLS 1.2 側と同様、成功/失敗にかかわらず増加する点に注意。

  now=$(date +%s)
  # 現在時刻取得。

  elapsed=$((now - start_time))
  # 経過秒数計算。

  if [ "$elapsed" -gt 30 ]; then
    # 30 秒を超えたら計測終了。
    break
  fi
done

echo "TLS1.3 full-handshake: ${count} connections in ${elapsed} real seconds"
# TLS 1.3 の結果表示。
#
# TLS 1.2 と TLS 1.3 の両方の結果を見比べることで、
# どちらの方が短時間に多くのフルハンドシェイクを処理できるかを比較できる。


# ------------------------------------------------------------
# 読み取りのポイント
# ------------------------------------------------------------
#
# このスクリプトの比較アルゴリズムをまとめると次のようになる。
#
#   TLS1.2:
#     30秒間ループして接続試行回数を数える
#
#   TLS1.3:
#     30秒間ループして接続試行回数を数える
#
#   最後に両者の count を比較する
#
# 図にすると:
#
#   TLS1.2 計測開始
#       ↓
#   接続試行を繰り返す
#       ↓
#   30秒超過で停止
#       ↓
#   回数表示
#       ↓
#   少し待つ
#       ↓
#   TLS1.3 計測開始
#       ↓
#   接続試行を繰り返す
#       ↓
#   30秒超過で停止
#       ↓
#   回数表示
#
# つまりこれは、
# 「固定時間でのスループット比較」
# に近いベンチマークである。
#
# 一方で、
# 「1 回の接続に何ミリ秒かかったか」
# を直接測るベンチマークではない。
#
# そのため、解釈としては
#
#   接続 1 回の厳密な遅延
#
# ではなく
#
#   一定時間でどれだけ回せるか
#
# を見る簡易比較だと理解するのが正しい。

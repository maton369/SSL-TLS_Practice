#!/bin/bash

# ============================================================
# TLS 1.2 / TLS 1.3 セッション再開 (resumption) 性能比較スクリプト
# ============================================================
#
# このスクリプトは、OpenSSL の `s_client` を用いて
#
#   1. まず 1 回フルハンドシェイクを行い、セッション情報をファイルへ保存する
#   2. その後、保存したセッション情報を `-sess_in` で再利用しながら
#      一定時間内に何回接続できるかを測定する
#
# という流れで、
#
#   - TLS 1.2 のセッション再開
#   - TLS 1.3 のセッション再開
#
# を比較するための簡易ベンチマークである。
#
# ここでいう「セッション再開」とは、
# 毎回フルハンドシェイクを最初からやり直すのではなく、
# 以前の接続で得たセッション情報を利用して、
# より軽量な接続確立を試みる仕組みである。
#
# したがって、このスクリプトのアルゴリズムは大きく分けて次の 2 段階になる。
#
#   第1段階: 初回接続でセッション情報を取得する
#   第2段階: 取得したセッション情報を使って再接続を繰り返す
#
# 図にすると次のようになる。
#
#   初回接続
#       ↓
#   セッション情報保存 (-sess_out sess)
#       ↓
#   while ループ開始
#       ↓
#   セッション再利用接続 (-sess_in sess)
#       ↓
#   count++
#       ↓
#   経過時間計算
#       ↓
#   30秒超過?
#     ├─ No → ループ継続
#     └─ Yes → 結果表示
#
# このスクリプトは「固定時間内の接続試行回数」を見るベンチマークであり、
# 1 接続ごとの厳密な遅延を測るものではない。
#
# また、元コードではバッククォート `` `date +%s` `` が使われていたが、
# 可読性のために `$(date +%s)` へ補完している。
# 意味は同じである。


# ------------------------------------------------------------
# TLS 1.2 セッション再開計測
# ------------------------------------------------------------

count=0
# count:
# 30 秒程度の間に、セッション再開つきの TLS 接続を何回試行できたかを数える変数。

elapsed=0
# elapsed:
# 計測開始からの経過秒数を保持する変数。


# まず 1 回、通常の TLS 1.2 接続を行い、
# そのとき得られたセッション情報を `sess` ファイルへ保存する。
#
# openssl s_client
#   OpenSSL 付属の TLS クライアントコマンド。
#
# -connect booktls-server:10443
#   接続先ホスト名とポート番号を指定する。
#   ここでは Docker ネットワーク内の `booktls-server` に対し、
#   10443 番ポートで接続する。
#
# -tls1_2
#   TLS 1.2 を明示的に使用する。
#
# -CAfile ca.pem
#   サーバ証明書を検証するための CA 証明書を指定する。
#   学習環境で自作 CA を使っているため、明示指定が必要になる。
#
# -sess_out sess
#   接続時に得られたセッション情報を `sess` というファイルへ保存する。
#   このファイルが後続の `-sess_in sess` で再利用される。
#
# <<< "Q"
#   bash のヒアストリング。
#   `s_client` は標準入力を待つことがあるので、ここでは "Q" を渡して
#   接続後すぐ終了しやすくしている。
#
# > /dev/null 2>&1
#   標準出力・標準エラー出力を捨てる。
#   ベンチマーク中の大量出力を防ぐためである。
#
# この 1 回目の接続は「計測対象」ではなく、
# あくまで「再開用セッション情報を作る準備接続」である点が重要である。
openssl s_client -connect booktls-server:10443 -tls1_2 -CAfile ca.pem -sess_out sess <<< "Q" > /dev/null 2>&1


start_time=$(date +%s)
# セッション再開ベンチマーク開始時刻を UNIX 時間（秒）で記録する。
# ここから先の while ループの所要時間を測る。


while true
do
  # ここでは、先ほど保存した `sess` を用いて TLS 1.2 の再接続を試みる。
  #
  # -sess_in sess
  #   以前保存したセッション情報を読み込んで接続に利用する。
  #   これにより、サーバがそのセッションを受け入れれば
  #   フルハンドシェイクではなく再開 (resumption) が行われる。
  #
  # TLS 1.2 における概念的な流れ:
  #
  #   初回:
  #     full handshake
  #       ↓
  #     session 保存
  #
  #   再接続:
  #     ClientHello + 以前の session 情報
  #       ↓
  #     サーバが受理すれば abbreviated handshake
  #
  # したがって、このループで測っているのは
  # 「TLS 1.2 のセッション再開を使った接続の回転数」である。
  openssl s_client -connect booktls-server:10443 -tls1_2 -CAfile ca.pem -sess_in sess <<< "Q" > /dev/null 2>&1

  ((count++))
  # 接続試行が 1 回終わるごとにカウンタを増やす。
  #
  # 注意:
  # 現在の書き方では `s_client` が失敗していても count は増える。
  # したがって、これは「成功回数」ではなく「試行回数」に近い。
  #
  # 成功した接続だけを数えたい場合は、直前コマンドの終了ステータスを見て
  # 成功時のみ count++ する実装の方が厳密である。

  now=$(date +%s)
  # 現在時刻を UNIX 時間（秒）で取得する。

  elapsed=$((now - start_time))
  # 経過秒数を求める。

  if [ "$elapsed" -gt 30 ]; then
    # 計測開始から 30 秒を超えたらループ終了。
    #
    # 注意:
    # 秒単位計測なので、厳密に 30.000 秒で止まるわけではなく、
    # 31 秒目で止まるなど多少の誤差はある。
    break
  fi
done

echo "TLS1.2 resumption: ${count} connections in ${elapsed} real seconds"
# TLS 1.2 セッション再開の結果を表示する。
#
# 例:
#   TLS1.2 resumption: 5000 connections in 31 real seconds
#
# これは
#   「約 31 秒間で 5000 回の再接続試行ができた」
# という意味である。


sleep 3
# 直前の計測の影響を少し和らげるために 3 秒待つ。
#
# たとえば以下の影響を多少抑える意図がある。
# - サーバ側の一時的な負荷
# - OS の TCP 状態
# - キャッシュや内部状態
#
# 完全に公平な制御ではないが、連続測定時の雑な干渉を減らす。


# ------------------------------------------------------------
# TLS 1.3 セッション再開計測
# ------------------------------------------------------------

count=0
# TLS 1.3 計測用にカウンタをリセットする。

elapsed=0
# 経過時間もリセットする。

start_time=$(date +%s)
# ここでは先に時刻を取っているが、
# 実際の TLS 1.3 側ではこの後に session を保存しているため、
# 厳密には「初回接続の準備時間」も elapsed に含まれる可能性がある。
#
# TLS 1.2 側は
#   sess_out の後で start_time
# TLS 1.3 側は
#   start_time の後で sess_out
#
# となっており、ここは測定条件の対称性が少し崩れている。
# 厳密比較をしたいなら、TLS 1.2 側と同じ順番に揃える方がよい。


# まず 1 回 TLS 1.3 で接続し、セッション再開用情報を `sess` に保存する。
#
# TLS 1.3 ではセッション再開は主に Session Ticket ベースで行われる。
# OpenSSL の `-sess_out` / `-sess_in` により、そのセッション情報を
# ファイル経由で再利用する。
#
# 注意:
# TLS 1.3 の場合、Session Ticket の扱いは TLS 1.2 と内部挙動が異なるが、
# このスクリプトの観点では「再利用可能なセッション情報を作る」という意味で
# 同じように扱ってよい。
openssl s_client -connect booktls-server:10443 -tls1_3 -CAfile ca.pem -sess_out sess <<< "Q" > /dev/null 2>&1


while true
do
  # TLS 1.3 のセッション再開を試みる接続。
  #
  # -tls1_3
  #   TLS 1.3 を明示的に使う。
  #
  # -sess_in sess
  #   直前の接続で保存したセッション情報を利用する。
  #
  # TLS 1.3 の概念的流れ:
  #
  #   初回:
  #     full handshake
  #       ↓
  #     NewSessionTicket 受信
  #       ↓
  #     session 保存
  #
  #   再接続:
  #     以前の PSK/Session 情報を使って resumption
  #
  # TLS 1.3 では TLS 1.2 よりセッション再開の枠組みが整理されているため、
  # 一般に効率の良い接続が期待される。
  openssl s_client -connect booktls-server:10443 -tls1_3 -CAfile ca.pem -sess_in sess <<< "Q" > /dev/null 2>&1

  ((count++))
  # 接続試行回数を 1 増やす。
  # ここも TLS 1.2 側と同様、終了ステータスに関係なく試行回数として数える。

  now=$(date +%s)
  # 現在時刻取得。

  elapsed=$((now - start_time))
  # 経過秒数計算。

  if [ "$elapsed" -gt 30 ]; then
    # 30 秒超過で計測終了。
    break
  fi
done

echo "TLS1.3 resumption: ${count} connections in ${elapsed} real seconds"
# TLS 1.3 セッション再開の結果を表示する。
#
# これにより、TLS 1.2 resumption と TLS 1.3 resumption の
# 接続スループットを比較できる。


# ------------------------------------------------------------
# このスクリプト全体の読み方
# ------------------------------------------------------------
#
# このベンチマークのアルゴリズムを 1 枚でまとめると次のようになる。
#
#   TLS 1.2:
#     1回フル接続して session 保存
#       ↓
#     30秒間 session 再利用接続を繰り返す
#       ↓
#     回数を表示
#
#   TLS 1.3:
#     1回フル接続して session 保存
#       ↓
#     30秒間 session 再利用接続を繰り返す
#       ↓
#     回数を表示
#
# つまり比較しているのは
#
#   「フルハンドシェイク性能」
#
# ではなく
#
#   「セッション再開を使ったときの接続回転性能」
#
# である。
#
# 図にすると次のようになる。
#
#   TLS1.2 初回接続
#       ↓
#   sess 保存
#       ↓
#   再接続ループ
#       ↓
#   30秒で停止
#       ↓
#   結果表示
#       ↓
#   少し待つ
#       ↓
#   TLS1.3 初回接続
#       ↓
#   sess 保存
#       ↓
#   再接続ループ
#       ↓
#   30秒で停止
#       ↓
#   結果表示
#
# したがって、解釈すべき対象は
#
#   「セッション再開ありの接続スループット」
#
# であって、
#
#   「TLS 1.2 / 1.3 の純粋なフルハンドシェイク速度差」
#
# とは別である。
#
# また、より厳密な比較をするなら次の改善が考えられる。
#
# - TLS 1.2 / TLS 1.3 で start_time の位置を揃える
# - 成功した接続だけ count する
# - `sess` を TLS 1.2 / 1.3 で別ファイルに分ける
# - 秒単位ではなく、より高精度なタイマを使う
# - 複数回測定して平均を取る

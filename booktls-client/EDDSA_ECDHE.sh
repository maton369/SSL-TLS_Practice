#!/bin/bash

# ============================================================
# OpenSSL 公開鍵暗号 / 鍵共有ベンチマークスクリプト
# ============================================================
#
# このスクリプトは OpenSSL の `speed` コマンドを使って、
# 次の公開鍵署名アルゴリズムおよび鍵共有アルゴリズムの性能を測定する。
#
# 署名系:
#   1. ECDSA P-256
#   2. Ed25519
#   3. ECDSA P-384
#   4. Ed448
#   5. ECDSA P-521
#
# 鍵共有系:
#   6. ECDH P-256
#   7. X25519
#   8. ECDH P-384
#   9. ECDH P-521
#
# さらに、前回の AES / ChaCha20 ベンチマークと同様に、
# 次の 2 条件でそれぞれ測定する。
#
#   - 通常状態
#   - AES-NI 無効化状態
#
# ------------------------------------------------------------
# このスクリプトで比較したいこと
# ------------------------------------------------------------
#
# 前回の AES-GCM / ChaCha20-Poly1305 では、
# AES-NI が対称鍵暗号の性能に大きく影響した。
#
# 一方、今回測定するのは主に
#
#   - 楕円曲線署名
#   - 楕円曲線 Diffie-Hellman
#   - Edwards 曲線系署名 / 鍵共有
#
# であり、AES 命令を直接使う処理ではない。
#
# そのため、AES-NI を無効化しても
#
#   「ほとんど差が出ない」
#
# ことが期待される。
#
# つまり今回の実験は、次の確認にもなる。
#
#   AES-NI の影響が大きいのは AES 系処理であり、
#   楕円曲線署名や鍵共有には本質的には効かない
#
# ------------------------------------------------------------
# アルゴリズムの流れ
# ------------------------------------------------------------
#
# このスクリプト全体の処理は次の順序で進む。
#
#   1. ログ保存先ディレクトリを作成する
#   2. 通常状態で署名ベンチマークを実行する
#   3. 通常状態で鍵共有ベンチマークを実行する
#   4. AES-NI 無効化状態で同じベンチマークを実行する
#   5. 結果をそれぞれ対応するログファイルへ保存する
#
# 図にすると次のようになる。
#
#   通常状態
#     ├─ ecdsap256 + ed25519        → log
#     ├─ ecdhp256 + ecdhx25519      → log
#     ├─ ecdsap384 + ed448 + ecdsap521 → log
#     └─ ecdhp384 + ecdhp521        → log
#
#   AES-NI 無効状態
#     ├─ ecdsap256 + ed25519        → log
#     ├─ ecdhp256 + ecdhx25519      → log
#     ├─ ecdsap384 + ed448 + ecdsap521 → log
#     └─ ecdhp384 + ecdhp521        → log
#
# ------------------------------------------------------------
# 入力コマンドに対する補完・整理点
# ------------------------------------------------------------
#
# 元の入力は次の 4 コマンドだった。
#
#   openssl speed ecdsap256 ed25519
#   openssl speed ecdhp256 ecdhx25519
#   openssl speed ecdsap384 ed448 ecdsap521
#   openssl speed ecdhp384 ecdhp521
#
# 今回は前回と同じ処理を追加してほしい、という要望に合わせて、
#
#   - 各コマンドをログファイルへ保存
#   - AES-NI 無効状態でも同じコマンドを実行
#   - 実行コマンドと開始・終了時刻をログに残す
#
# という枠組みを追加している。
#
# ------------------------------------------------------------
# AES-NI 無効化について
# ------------------------------------------------------------
#
# `OPENSSL_ia32cap="~0x200000200000000"` は、
# OpenSSL に対して x86 CPU capability の一部を隠し、
# AES-NI などのハードウェア支援機能を見えなくするための環境変数設定である。
#
# ただし今回の測定対象は AES そのものではないため、
# 結果差は小さい、あるいはほぼ出ないことが予想される。
#
# それでも前回と同じ枠組みで計測することで、
#
#   「AES-NI は楕円曲線署名や ECDH の性能にはほぼ影響しない」
#
# という確認ができる。
#
# ------------------------------------------------------------
# ログの扱い
# ------------------------------------------------------------
#
# 実行結果はそれぞれ対応するログファイルへ保存する。
#
# 例:
#   logs/ec_sign_default.log
#   logs/ec_kex_default.log
#   logs/ec_sign_high_default.log
#   logs/ec_kex_high_default.log
#   logs/ec_sign_no_aesni.log
#   logs/ec_kex_no_aesni.log
#   logs/ec_sign_high_no_aesni.log
#   logs/ec_kex_high_no_aesni.log
#
# 画面にも表示しつつ、対応するログファイルへ保存するため、
# `tee` を使っている。


# エラー時に即停止する設定。
# -e : コマンド失敗時に終了
# -u : 未定義変数使用時に終了
# -o pipefail : パイプライン中の失敗も検知
set -euo pipefail


# ログ保存先ディレクトリ
LOG_DIR="logs"

# ログディレクトリが無ければ作成する
mkdir -p "${LOG_DIR}"


# ------------------------------------------------------------
# run_bench:
#   ベンチマークコマンドを実行し、結果をログへ保存する補助関数
#
# 引数:
#   $1 : ログファイルパス
#   $2以降 : 実行するコマンド
#
# 役割:
#   - 実行コマンドを画面とログに出す
#   - ベンチマーク結果をログに残す
#   - 標準エラーもログへ含める
# ------------------------------------------------------------
run_bench() {
  local logfile="$1"
  shift

  {
    echo "============================================================"
    echo "Benchmark started: $(date)"
    echo "Log file: ${logfile}"
    echo "Command: $*"
    echo "============================================================"
    "$@"
    echo
    echo "============================================================"
    echo "Benchmark finished: $(date)"
    echo "============================================================"
  } 2>&1 | tee "${logfile}"
}


# ============================================================
# 1. 通常状態でのベンチマーク
# ============================================================

# ------------------------------------------------------------
# 署名系ベンチマーク その1
#
# ecdsap256
#   ECDSA over P-256 の署名/検証性能を測定する
#
# ed25519
#   Ed25519 の署名/検証性能を測定する
#
# `openssl speed` では、署名系アルゴリズムについて
# sign/s verify/s のような指標が表示されることが多い。
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_sign_default.log" \
  openssl speed ecdsap256 ed25519


# ------------------------------------------------------------
# 鍵共有系ベンチマーク その1
#
# ecdhp256
#   ECDH over P-256 の鍵共有性能を測定する
#
# ecdhx25519
#   X25519 の鍵共有性能を測定する
#
# これらは TLS の鍵交換部分に対応する性能を見たいときに重要である。
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_kex_default.log" \
  openssl speed ecdhp256 ecdhx25519


# ------------------------------------------------------------
# 署名系ベンチマーク その2
#
# ecdsap384
#   ECDSA over P-384
#
# ed448
#   Ed448
#
# ecdsap521
#   ECDSA over P-521
#
# セキュリティ強度が高い曲線ほど、一般に計算量も増えるため、
# P-256 / Ed25519 と比較してどう差が出るかが観察ポイントになる。
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_sign_high_default.log" \
  openssl speed ecdsap384 ed448 ecdsap521


# ------------------------------------------------------------
# 鍵共有系ベンチマーク その2
#
# ecdhp384
#   ECDH over P-384
#
# ecdhp521
#   ECDH over P-521
#
# P-256 / X25519 と比べてどれくらい重くなるかを見る。
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_kex_high_default.log" \
  openssl speed ecdhp384 ecdhp521


# ============================================================
# 2. AES-NI を無効にした状態でのベンチマーク
# ============================================================

# ------------------------------------------------------------
# 署名系ベンチマーク その1 (AES-NI 無効)
#
# ここでは前回と同様に AES-NI を見えなくするが、
# ECDSA / Ed25519 自体は AES 命令に依存しない。
# そのため、結果差が小さいことが期待される。
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_sign_no_aesni.log" \
  env OPENSSL_ia32cap="~0x200000200000000" \
  openssl speed ecdsap256 ed25519


# ------------------------------------------------------------
# 鍵共有系ベンチマーク その1 (AES-NI 無効)
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_kex_no_aesni.log" \
  env OPENSSL_ia32cap="~0x200000200000000" \
  openssl speed ecdhp256 ecdhx25519


# ------------------------------------------------------------
# 署名系ベンチマーク その2 (AES-NI 無効)
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_sign_high_no_aesni.log" \
  env OPENSSL_ia32cap="~0x200000200000000" \
  openssl speed ecdsap384 ed448 ecdsap521


# ------------------------------------------------------------
# 鍵共有系ベンチマーク その2 (AES-NI 無効)
# ------------------------------------------------------------
run_bench \
  "${LOG_DIR}/ec_kex_high_no_aesni.log" \
  env OPENSSL_ia32cap="~0x200000200000000" \
  openssl speed ecdhp384 ecdhp521


# ============================================================
# 3. 実行結果の保存先一覧を表示
# ============================================================
echo
echo "Benchmark logs saved to:"
echo "  ${LOG_DIR}/ec_sign_default.log"
echo "  ${LOG_DIR}/ec_kex_default.log"
echo "  ${LOG_DIR}/ec_sign_high_default.log"
echo "  ${LOG_DIR}/ec_kex_high_default.log"
echo "  ${LOG_DIR}/ec_sign_no_aesni.log"
echo "  ${LOG_DIR}/ec_kex_no_aesni.log"
echo "  ${LOG_DIR}/ec_sign_high_no_aesni.log"
echo "  ${LOG_DIR}/ec_kex_high_no_aesni.log"


# ============================================================
# 読み取りのポイント
# ============================================================
#
# このスクリプトの結果を読むときは、次の比較が重要である。
#
# 1. P-256 vs P-384 vs P-521
#    → 曲線サイズが大きいほど重くなるか
#
# 2. ECDSA vs Ed25519 / Ed448
#    → 署名系アルゴリズムの効率差
#
# 3. ECDH P-256 vs X25519
#    → 鍵共有の効率差
#
# 4. 通常状態 vs AES-NI 無効状態
#    → AES 命令支援が楕円曲線処理へほとんど影響しないことの確認
#
# つまり今回の実験は、
#
#   「AES 系暗号とは違って、楕円曲線署名・鍵共有は AES-NI に依存しない」
#
# という点を定量的に確認する意味も持っている。

#!/bin/bash

# このスクリプトは、既存の CSR（証明書署名要求） `sample.csr` と
# 既存の秘密鍵 `sample.key` を使って、
# **自己署名証明書（self-signed certificate）** を生成するためのものです。
#
# ここでいう「自己署名」とは、
# 外部の CA（認証局）に署名してもらうのではなく、
# **自分自身の秘密鍵で自分の CSR に署名して証明書を作る**
# という意味です。
#
# TLS / PKI の流れの中では、これは次の位置にあります。
#
#   1. 秘密鍵生成
#   2. CSR 作成
#   3. 自己署名証明書の生成   ← このスクリプト
#   4. nginx / Apache / OpenSSL サーバ等で利用
#
# 学習用途・ローカル検証・閉じた実験環境では自己署名証明書がよく使われます。
# ただし、公開サービスでは通常は信頼された CA に署名してもらう必要があります。
#
# ------------------------------------------------------------------
# 重要な修正点
# ------------------------------------------------------------------
# 元のコードは次のようになっていました。
#
#   openssl x509 -req -days 180 -signkey sample.key -in sample.csr -out sample.csr
#
# しかし、これは出力先が `sample.csr` になっており、
# **入力 CSR ファイルを上書きしてしまう危険** があります。
#
# CSR は「証明書署名要求」であり、証明書とは別物です。
# したがって、出力先は通常 `sample.crt` や `sample.pem` など、
# **証明書ファイル名として別に分ける** のが正しいです。
#
# このスクリプトでは、安全のため出力先を
#
#   sample.crt
#
# に修正しています。


# エラーが発生した時点でスクリプトを停止する設定です。
# -e : いずれかのコマンドが失敗したら終了
# -u : 未定義変数を使ったらエラー
# -o pipefail : パイプライン途中の失敗も検出
set -euo pipefail


# 入力となる秘密鍵ファイルです。
# これは `openssl genpkey` などで事前に生成しておく必要があります。
KEY_FILE="sample.key"

# 入力となる CSR（証明書署名要求）ファイルです。
# これは `openssl req -new ...` などで事前に生成しておく必要があります。
CSR_FILE="sample.csr"

# 出力する自己署名証明書ファイルです。
# 元コードでは誤って sample.csr を上書きしようとしていたため、
# 証明書用のファイル名として sample.crt を用います。
CERT_FILE="sample.crt"


# 秘密鍵ファイルの存在確認です。
# 無い場合は自己署名できないため、先に明示的に止めます。
if [[ ! -f "${KEY_FILE}" ]]; then
  echo "[ERROR] 秘密鍵ファイル ${KEY_FILE} が存在しません。"
  echo "[INFO] 先に秘密鍵を生成してください。"
  exit 1
fi

# CSR ファイルの存在確認です。
# 無い場合は証明書を生成できないため、先に明示的に止めます。
if [[ ! -f "${CSR_FILE}" ]]; then
  echo "[ERROR] CSR ファイル ${CSR_FILE} が存在しません。"
  echo "[INFO] 先に CSR を生成してください。"
  exit 1
fi


# openssl x509
# 証明書を扱う OpenSSL コマンドです。
#
# -req
#   入力が CSR（certificate request）であることを示します。
#   つまり、`-in sample.csr` を「証明書署名要求」として読み込みます。
#
# -days 180
#   生成する証明書の有効期限を 180 日に設定します。
#   これは「今日から 180 日有効な証明書」を意味します。
#
# -signkey "${KEY_FILE}"
#   指定した秘密鍵で CSR に署名し、自己署名証明書を作成します。
#   ここが「自己署名」の核心です。
#
# -in "${CSR_FILE}"
#   入力となる CSR ファイルを指定します。
#   CSR に含まれる Subject 情報や公開鍵を読み込みます。
#
# -out "${CERT_FILE}"
#   生成した証明書の保存先です。
#   証明書は CSR とは別ファイルに保存する必要があります。
#
# 内部的な処理の流れは概ね次の通りです。
#
#   1. CSR を読み込む
#   2. CSR から Subject と公開鍵を取り出す
#   3. 証明書構造を作る
#   4. 有効期限を設定する
#   5. 秘密鍵で署名する
#   6. X.509 証明書として出力する
#
# 図にするとこうです。
#
#   sample.csr
#       ↓
#   Subject / Public Key を抽出
#       ↓
#   X.509 証明書構造を作成
#       ↓
#   sample.key で署名
#       ↓
#   sample.crt を出力
#
# 注意:
# - `sample.key` がパスフレーズ付きで暗号化されている場合、
#   実行時にパスフレーズ入力を求められます。
# - 自己署名証明書は公開 CA による信頼は得られません。
#   そのため、ブラウザや一般クライアントでは通常警告が出ます。
openssl x509 \
  -req \
  -days 180 \
  -signkey "${KEY_FILE}" \
  -in "${CSR_FILE}" \
  -out "${CERT_FILE}"


# 出力された証明書ファイルの存在確認です。
if [[ -f "${CERT_FILE}" ]]; then
  echo "[INFO] 自己署名証明書 ${CERT_FILE} を生成しました。"
else
  echo "[ERROR] 証明書ファイル ${CERT_FILE} の生成に失敗しました。"
  exit 1
fi


# 実行後に確認したいコマンド例:
#
# 1. 証明書の中身を確認する
#    openssl x509 -in sample.crt -text -noout
#
# 2. 証明書の Subject を確認する
#    openssl x509 -in sample.crt -noout -subject
#
# 3. 証明書の発行者 (issuer) を確認する
#    openssl x509 -in sample.crt -noout -issuer
#
#    自己署名証明書の場合、通常は Subject と Issuer が同じになります。
#
# 4. 証明書の有効期限を確認する
#    openssl x509 -in sample.crt -noout -dates
#
# セキュリティ上の注意:
# - 自己署名証明書は学習用・ローカル用途には便利ですが、
#   公開サービス用途には通常不向きです。
# - 実運用では CA 署名証明書を用いるのが基本です。
# - 秘密鍵 `sample.key` は引き続き厳重に保護する必要があります。

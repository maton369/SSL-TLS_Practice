#!/bin/bash

# ============================================================
# RSA秘密鍵生成スクリプト
#
# このスクリプトは OpenSSL を使用して RSA 秘密鍵を生成する。
# 生成された秘密鍵は AES-256 によって暗号化された状態で保存される。
#
# TLS / PKI のワークフローでは以下の用途で使用される。
#
#   ・TLS サーバ証明書の秘密鍵
#   ・CSR (Certificate Signing Request) の作成
#   ・自己署名証明書の生成
#   ・テスト用 PKI の構築
#
# 内部的には以下のアルゴリズムが実行される。
#
#   1. 暗号学的乱数生成
#   2. RSA鍵生成
#   3. 秘密鍵のAES暗号化
#   4. PEM形式でファイル保存
#
# 注意
#
#   ・秘密鍵は重要な機密情報である
#   ・生成後はアクセス権を制限すること
#
# ============================================================


# ------------------------------------------------------------
# RSA秘密鍵を生成する
#
# openssl genpkey
#   OpenSSLの汎用秘密鍵生成コマンド
#
# -algorithm RSA
#   RSA公開鍵暗号アルゴリズムを使用
#
# -aes256
#   秘密鍵をAES-256で暗号化して保存
#
# -out sample.key
#   出力ファイル名
#
# -pkeyopt rsa_keygen_bits:2048
#   RSA鍵長 (2048bit)
# ------------------------------------------------------------

openssl genpkey \
  -algorithm RSA \
  -aes256 \
  -out sample.key \
  -pkeyopt rsa_keygen_bits:2048


# ------------------------------------------------------------
# 秘密鍵ファイルの権限を制限
#
# 秘密鍵は所有者のみ読み書き可能にする
# ------------------------------------------------------------

chmod 600 sample.key


# ------------------------------------------------------------
# 生成結果の確認
#
# openssl pkey
#   秘密鍵の内容を表示する
#
# -text
#   鍵情報を人間が読める形式で表示
#
# -noout
#   PEM本文は表示しない
# ------------------------------------------------------------

openssl pkey -in sample.key -text -noout
